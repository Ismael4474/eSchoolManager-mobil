package com.ismaellopez.eschoolmanager_mobil.modelo.sessio;

public class Sessio {

    private int codiSessio;
    private String nomEstudiant;
    private String cognomsEstudiant;
    private String nomEmpleat;
    private String cognomsEmpleat;
    private String nomServei;
    private String dataSessio;

    public int getCodiSessio() {
        return codiSessio;
    }

    public void setCodiSessio(int codiSessio) {
        this.codiSessio = codiSessio;
    }

    public String getNomEstudiant() {
        return nomEstudiant;
    }

    public void setNomEstudiant(String nomEstudiant) {
        this.nomEstudiant = nomEstudiant;
    }

    public String getCognomsEstudiant() {
        return cognomsEstudiant;
    }

    public void setCognomsEstudiant(String cognomsEstudiant) {
        this.cognomsEstudiant = cognomsEstudiant;
    }

    public String getNomEmpleat() {
        return nomEmpleat;
    }

    public void setNomEmpleat(String nomEmpleat) {
        this.nomEmpleat = nomEmpleat;
    }

    public String getCognomsEmpleat() {
        return cognomsEmpleat;
    }

    public void setCognomsEmpleat(String cognomsEmpleat) {
        this.cognomsEmpleat = cognomsEmpleat;
    }

    public String getNomServei() {
        return nomServei;
    }

    public void setNomServei(String nomServei) {
        this.nomServei = nomServei;
    }

    public String getDataSessio() {
        return dataSessio;
    }

    public void setDataSessio(String dataSessio) {
        this.dataSessio = dataSessio;
    }
}
